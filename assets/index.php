<h1>Tipos de Amortización </h1>
<section id="amortizacion">
<h1> Amortización Constante:</h1>
<p> Amortización Constante:
- En este tipo de amortización, los pagos de capital son constantes, lo que significa que el importe pagado del principal no cambia. Sin embargo, los intereses disminuyen con el tiempo, lo que provoca que los pagos totales (capital + intereses) disminuyan progresivamente.
</p>
</section>
