<section id="diferencia_amortizacion">
    <h1>Diferencia entre Amortización y Depreciación </h1>
  <p> Aunque ambos términos implican la reducción del valor de un activo, la amortización se aplica principalmente a activos intangibles y deudas, mientras que la depreciación se refiere a activos físicos o tangibles.

</p>
</section>
