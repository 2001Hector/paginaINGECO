<section id="importancia_amortizacion">
    <h1>La importancia de la amortizacion </h1>
  <p> La amortización permite a los prestatarios cumplir con sus obligaciones de manera organizada y accesible. Además, es una herramienta financiera clave para calcular el valor de los activos intangibles a lo largo del tiempo, como las patentes o los derechos de autor.

</p>
</section>

