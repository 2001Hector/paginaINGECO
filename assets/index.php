<h1>Capitalización </h1>
<section id="definicion_capitalizacion">
    <h1>definicion capitalizacion</h1>
  <p>  La capitalización es el proceso de convertir ingresos en valor presente, o en términos financieros, sumar los intereses generados a la cantidad principal para que a partir de ese momento también generen intereses. Esto implica reinvertir los intereses, generando lo que se conoce como "interés compuesto"
 
</p>
</section> <br><BR>
